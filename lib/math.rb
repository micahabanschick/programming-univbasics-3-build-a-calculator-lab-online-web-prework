def addition(num1, num2)
  sum = num1 + num2
  return sum 
end

def subtraction(num1, num2)
  dif = num1 - num2
  return dif 
end

def division(num1, num2)
  quo = num1 / num2
  return quo 
end

def multiplication(num1, num2)
  pro = num1 * num2
  return pro 
end

def modulo(num1, num2)
  mod = num1 % num2
  return mod 
end

def square_root(num)
  return Math.sqrt(num) 
end